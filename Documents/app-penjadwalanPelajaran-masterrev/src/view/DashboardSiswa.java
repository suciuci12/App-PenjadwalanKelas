    private void tampilkanJadwal() {
        // Buat frame atau dialog khusus untuk menampilkan tabel jadwal
        JDialog dialog = new JDialog(this, "Jadwal Pelajaran", true);
        dialog.setSize(600, 400);
        dialog.setLocationRelativeTo(this);

        JTable tableJadwal = new JTable();
        JScrollPane scrollPane = new JScrollPane(tableJadwal);
        dialog.add(scrollPane);

        // Load data jadwal
        loadJadwalKelas(tableJadwal);

        dialog.setVisible(true);
    }

    private void loadJadwalKelas(JTable tableJadwal) {
        try {
            Connection conn = config.DatabaseConnection.getConnection();
            String sql = "SELECT p.nama_pelajaran, g.nama_guru, j.hari, j.jam_mulai, j.jam_selesai " +
                         "FROM jadwal j " +
                         "JOIN pelajaran p ON j.pelajaran_id = p.id " +
                         "JOIN guru g ON j.guru_id = g.id " +
                         "WHERE j.kelas_id=?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setInt(1, kelasId);
            ResultSet rs = pst.executeQuery();

            DefaultTableModel model = new DefaultTableModel(
                new String[]{"Pelajaran", "Guru", "Hari", "Jam Mulai", "Jam Selesai"}, 0
            );

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("nama_pelajaran"),
                    rs.getString("nama_guru"),
                    rs.getString("hari"),
                    rs.getString("jam_mulai"),
                    rs.getString("jam_selesai")
                });
            }

            tableJadwal.setModel(model);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Gagal memuat jadwal: " + e.getMessage());
        }
    }
    
