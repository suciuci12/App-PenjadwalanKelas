    private void setComboSelectedItem(javax.swing.JComboBox<String> combo, String value) {
        for (int i = 0; i < combo.getItemCount(); i++) {
            String item = combo.getItemAt(i);
            if (item.trim().equalsIgnoreCase(value.trim())) {
                combo.setSelectedIndex(i);
                return;
            }
        }
        // Jika tidak ketemu, tetap pilih index 0
        combo.setSelectedIndex(0);
    }


    private void tampilDataSiswa() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("NIS");
        model.addColumn("Nama");
        model.addColumn("JK");
        model.addColumn("Tanggal Lahir");
        model.addColumn("Kelas");
        model.addColumn("Jurusan");

        try {
            String sql = "SELECT siswa.nis, nama_siswa, jenis_kelamin, tanggal_lahir, kelas.nama_kelas, jurusan.nama_jurusan " +
                         "FROM siswa " +
                         "JOIN kelas ON siswa.id_kelas = kelas.id " +
                         "JOIN jurusan ON siswa.id_jurusan = jurusan.id";

            Connection conn = DatabaseConnection.getConnection();
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(sql);

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("nis"),
                    rs.getString("nama_siswa"),
                    rs.getString("jenis_kelamin"),
                    rs.getDate("tanggal_lahir"),
                    rs.getString("nama_kelas"),
                    rs.getString("nama_jurusan")
                });
            }

            tableSiswa.setModel(model);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal tampil data: " + e.getMessage());
        }
    }
    
    private int getIdKelas(String nama) throws SQLException {
        Connection conn = DatabaseConnection.getConnection();
        PreparedStatement pst = conn.prepareStatement("SELECT id FROM kelas WHERE nama_kelas=?");
        pst.setString(1, nama);
        ResultSet rs = pst.executeQuery();
        if (rs.next()) return rs.getInt("id");
        return 0;
    }

    private int getIdJurusan(String nama) throws SQLException {
        Connection conn = DatabaseConnection.getConnection();
        PreparedStatement pst = conn.prepareStatement("SELECT id FROM jurusan WHERE nama_jurusan=?");
        pst.setString(1, nama);
        ResultSet rs = pst.executeQuery();
        if (rs.next()) return rs.getInt("id");
        return 0;
    }


